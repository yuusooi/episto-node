"""Automated evaluation harness for the Episto Examiner Agent.

Implements the LLM-as-a-Judge pattern: a high-capability model (DeepSeek-R1 or
GPT-4o) evaluates the quality of exam papers generated by the Episto Examiner
sub-agent. This enables regression testing — run after any prompt change to
verify the average score hasn't dropped.

Usage:
    python tests/test_eval.py

Evaluation dimensions (each scored 1-5):
    1. Format — Is the output a valid, well-structured exam paper?
    2. Accuracy — Are the questions technically correct?
    3. Coverage — Do the questions cover the expected concepts?
    4. Difficulty — Does the difficulty match the request?

Reference: LangSmith "LLM-as-a-Judge" evaluation pattern.
    https://docs.smith.langchain.com/evaluation

NOTE: The judge model must be a high-capability model. Using a small model
as judge produces unreliable scores. We default to deepseek-chat (DeepSeek-V3)
which is the same model used by the agent — in production you'd want a
stronger model like deepseek-reasoner or gpt-4o as judge.
"""

import json
import os
import sys
import time

# Fix: conda env sets SSL_CERT_FILE to a non-existent path
os.environ.pop("SSL_CERT_FILE", None)

# Ensure the backend package is importable
sys.path.insert(0, os.path.join(os.path.dirname(__file__), "..", "backend"))

from dotenv import load_dotenv
from langchain_core.messages import HumanMessage
from langchain_openai import ChatOpenAI

load_dotenv()

# Configuration
# Path to the golden dataset
DATASET_PATH = os.path.join(os.path.dirname(__file__), "dataset.json")

# Judge model — uses DeepSeek by default.
# For stricter evaluation, switch to deepseek-reasoner or an OpenAI model.
JUDGE_MODEL = os.getenv("EPISTO_JUDGE_MODEL", "deepseek-chat")

# Scoring prompt for the judge model
JUDGE_PROMPT_TEMPLATE = """\
你是一个严苛的前端与技术教育专家。请评估以下自动生成的考卷质量。

## 生成指令
用户输入: {input}
期望覆盖的概念: {expected_concept}
期望题目数量: {expected_num_questions}
期望难度: {difficulty}

## 生成的考卷
{exam_paper}

## 评分维度（每项 1-5 分）

1. **格式规范性** (format): 考卷是否为规范的 JSON 结构？题目是否包含
   question、options、answer、explanation 字段？选项是否为 4 个？
2. **题目准确性** (accuracy): 题目内容是否技术正确？答案是否准确？
   解析是否合理？有没有明显的错误？
3. **概念覆盖度** (coverage): 题目是否覆盖了期望的概念
   ({expected_concept})？是否有题目在考不相关的知识？
4. **难度匹配度** (difficulty_match): 题目难度是否符合期望的
   {difficulty} 级别？

## 输出格式

请严格按以下 JSON 格式输出，不要包含其他文字：
```json
{{
  "format": <1-5>,
  "accuracy": <1-5>,
  "coverage": <1-5>,
  "difficulty_match": <1-5>,
  "average": <1-5的浮点数>,
  "comment": "<一句话点评>"
}}
```"""

# Evaluation logic
def load_dataset() -> list[dict]:
    """Load the golden dataset from dataset.json.

    Returns:
        List of test case dicts with id, input, expected_concept, etc.
    """
    with open(DATASET_PATH, encoding="utf-8") as f:
        return json.load(f)


def run_examiner(test_case: dict) -> dict | None:
    """Run the Episto graph for a single test case and extract the exam paper.

    Uses the full LangGraph pipeline: the Lead Agent receives the user input
    and routes it to the Examiner sub-agent, which generates the exam paper.

    Args:
        test_case: A single test case from the golden dataset.

    Returns:
        The exam_paper dict from the graph state, or None if generation failed.
    """
    from episto.graph import make_graph

    graph = make_graph()

    # Each test case gets its own thread to avoid state contamination
    thread_id = f"eval_test_{test_case['id']}"
    config = {"configurable": {"thread_id": thread_id}}

    # Invoke the graph with the test input
    init_state = {"messages": [("user", test_case["input"])]}

    try:
        result = graph.invoke(init_state, config=config)
        exam_paper = result.get("exam_paper")
        return exam_paper
    except Exception as e:
        print(f"  [ERROR] Graph execution failed for test case {test_case['id']}: {e}")
        return None


def judge_exam(test_case: dict, exam_paper: dict) -> dict:
    """Use a judge LLM to evaluate the quality of an exam paper.

    Sends the exam paper along with expected criteria to a high-capability
    model for scoring across four dimensions.

    Args:
        test_case: The test case with expected concepts and difficulty.
        exam_paper: The generated exam paper dict.

    Returns:
        Scoring dict with format, accuracy, coverage, difficulty_match, average,
        and comment. Returns default low scores on parse failure.
    """
    judge_model = ChatOpenAI(
        model=JUDGE_MODEL,
        openai_api_key=os.getenv("DEEPSEEK_API_KEY"),
        openai_api_base=os.getenv("DEEPSEEK_BASE_URL"),
        temperature=0,
    )

    # Format exam paper for display
    exam_text = json.dumps(exam_paper, ensure_ascii=False, indent=2)

    prompt = JUDGE_PROMPT_TEMPLATE.format(
        input=test_case["input"],
        expected_concept=test_case["expected_concept"],
        expected_num_questions=test_case["expected_num_questions"],
        difficulty=test_case["difficulty"],
        exam_paper=exam_text,
    )

    response = judge_model.invoke([HumanMessage(content=prompt)])

    # Parse the JSON score from the judge response
    import re

    text = response.content
    json_match = re.search(r"```json\s*(.*?)\s*```", text, re.DOTALL)
    if json_match:
        try:
            scores = json.loads(json_match.group(1))
            return scores
        except json.JSONDecodeError:
            pass

    # Fallback: try parsing the whole response as JSON
    try:
        return json.loads(text)
    except json.JSONDecodeError:
        print(f"  [WARN] Judge returned non-JSON response: {text[:200]}")
        return {
            "format": 1,
            "accuracy": 1,
            "coverage": 1,
            "difficulty_match": 1,
            "average": 1.0,
            "comment": f"Failed to parse judge response",
        }


def main():
    """Run the full evaluation harness.

    Loads the golden dataset, runs the Examiner for each test case,
    sends results to the judge model, and prints a summary report.
    """
    print("=" * 60)
    print("  Episto Eval Harness — LLM-as-a-Judge")
    print(f"  Judge Model: {JUDGE_MODEL}")
    print(f"  Dataset: {DATASET_PATH}")
    print("=" * 60)

    dataset = load_dataset()
    print(f"\nLoaded {len(dataset)} test cases from dataset.json\n")

    all_scores = []
    results = []

    for i, test_case in enumerate(dataset):
        print(f"[{i+1}/{len(dataset)}] Test Case #{test_case['id']}")
        print(f"  Input: {test_case['input']}")

        # Step 1: Run the Examiner through the full graph
        print("  Running graph...")
        exam_paper = run_examiner(test_case)

        if exam_paper is None:
            print("  SKIPPED — graph execution failed\n")
            continue

        num_questions = len(exam_paper.get("questions", []))
        print(f"  Generated {num_questions} questions")

        # Step 2: Send to judge model for scoring
        print("  Judging...")
        scores = judge_exam(test_case, exam_paper)

        avg = scores.get("average", 0)
        comment = scores.get("comment", "")
        all_scores.append(avg)

        print(f"  Scores: format={scores.get('format', '?')}, "
              f"accuracy={scores.get('accuracy', '?')}, "
              f"coverage={scores.get('coverage', '?')}, "
              f"difficulty={scores.get('difficulty_match', '?')}")
        print(f"  Average: {avg:.1f} / 5.0")
        print(f"  Comment: {comment}\n")

        results.append({
            "test_case_id": test_case["id"],
            "input": test_case["input"],
            "num_questions_generated": num_questions,
            "scores": scores,
        })

        # Rate limit: avoid hitting API rate limits between test cases
        if i < len(dataset) - 1:
            time.sleep(2)

    # Step 3: Print summary report
    print("=" * 60)
    print("  SUMMARY REPORT")
    print("=" * 60)

    if all_scores:
        overall_avg = sum(all_scores) / len(all_scores)
        print(f"\n  Total test cases evaluated: {len(all_scores)} / {len(dataset)}")
        print(f"  Average Score: {overall_avg:.2f} / 5.0")
        print(f"  Min Score: {min(all_scores):.1f}")
        print(f"  Max Score: {max(all_scores):.1f}")

        # Verdict
        if overall_avg >= 4.5:
            verdict = "EXCELLENT"
        elif overall_avg >= 3.5:
            verdict = "GOOD"
        elif overall_avg >= 2.5:
            verdict = "NEEDS IMPROVEMENT"
        else:
            verdict = "POOR — regression detected"

        print(f"\n  Verdict: {verdict}")
    else:
        print("\n  No test cases were successfully evaluated.")

    print()

    # Save detailed results to JSON for further analysis
    results_path = os.path.join(os.path.dirname(__file__), "eval_results.json")
    with open(results_path, "w", encoding="utf-8") as f:
        json.dump(results, f, ensure_ascii=False, indent=2)
    print(f"  Detailed results saved to: {results_path}")


if __name__ == "__main__":
    main()
